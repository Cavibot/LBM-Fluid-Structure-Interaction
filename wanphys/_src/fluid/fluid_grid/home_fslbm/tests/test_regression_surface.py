def _init_rest_state(domain, N):
    """Set f_mom/f_mom_post to equilibrium (rho=1, u=0).

    .. note::

        ``tag_matrix`` is left at the Warp default (zeros).  This is
        intentional: zero-valued tags trigger the strain-rate turbulence
        path, producing the same effective omega (approx 1.998) as the
        reference code's hardcoded ``1/((1e-4)*3+0.5)``.  If tags were
        set to -1, Warp would use ``model.omega`` (default 1.0) while
        the reference still uses 1.998, causing a collision-relaxation
        mismatch.
    """
    import warp as wp

    state = domain.state
    total = N * N * N

    # Equilibrium moments: rho=1, u=0, S_ab=0
    f_data = np.zeros(10 * total, dtype=np.float32)
    f_data[0 * total:1 * total] = 1.0  # rho
    # u = 0, S = 0 (already zero)

    f_mom_arr = wp.array(f_data, dtype=float, device=state.f_mom.device)
    wp.copy(state.f_mom, f_mom_arr)
    wp.copy(state.f_mom_post, f_mom_arr)

    # massex = 0
    state.massex.zero_()

    # force = 0 (will be set by gravity in step)
    state.force_x.zero_()
    state.force_y.zero_()
    state.force_z.zero_()

    # delta_phi = 0
    state.delta_phi.zero_()


def _run_steps(domain, steps):
    """Run `steps` domain steps and return the final state arrays."""
    for _ in range(steps):
        domain.step(dt=1.0)


# ============================================================================
# Cell-ordering conversion (ref k-major → Warp i-major)
# ============================================================================
# Reference C flat-index: idx_ref = k*N*N + j*N + i  (k slowest, i fastest)
# Warp flat-index:        idx_warp = i*N*N + j*N + k  (i slowest, k fastest)


def _reorder_scalar_field(golden_flat: "np.ndarray", N: int) -> "np.ndarray":
    """Convert a flat scalar field (N**3) from ref (k-major) to Warp (i-major).

    golden_flat[k*N*N + j*N + i]  →  out[i*N*N + j*N + k]
    """
    g3d = golden_flat.reshape(N, N, N)          # [k, j, i] in C order
    w3d = np.transpose(g3d, (2, 1, 0))           # [i, j, k]
    return w3d.ravel()


def _reorder_f_mom_post(golden_flat: "np.ndarray", N: int) -> "np.ndarray":
    """Convert f_mom_post (10*N**3 interleaved) from ref to Warp ordering.

    Layout: [mom0_cell0, mom0_cell1, ..., mom1_cell0, ...]
    """
    total = N * N * N
    out = np.empty_like(golden_flat)
    for m in range(10):
        out[m * total : (m + 1) * total] = _reorder_scalar_field(
            golden_flat[m * total : (m + 1) * total], N,
        )
    return out


