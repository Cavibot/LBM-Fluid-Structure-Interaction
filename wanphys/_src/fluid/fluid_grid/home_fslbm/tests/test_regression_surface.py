def _setup_sphere_droplet(domain, N, R, cx=None, cy=None, cz=None):
    """Initialise flag/phi/mass for a sphere droplet.

    Uses phi = clamp((R - dist) / delta + 0.5, 0, 1) with delta = 1.5 dx.
    """
    import warp as wp

    if cx is None:
        cx = N / 2
    if cy is None:
        cy = N / 2
    if cz is None:
        cz = N / 2

    delta = 1.5
    phi_host = np.zeros((N, N, N), dtype=np.float32)
    flag_host = np.zeros((N, N, N), dtype=np.uint8)
    mass_host = np.zeros((N, N, N), dtype=np.float32)

    for k in range(N):
        for j in range(N):
            for i in range(N):
                dx = i - cx
                dy = j - cy
                dz = k - cz
                dist = np.sqrt(dx * dx + dy * dy + dz * dz)
                phi_raw = (R - dist) / delta + 0.5
                phi_val = max(0.0, min(1.0, phi_raw))
                phi_host[i, j, k] = phi_val
                if phi_val >= 1.0 - 1e-6:
                    flag_host[i, j, k] = C.CellFlag.TYPE_F
                    mass_host[i, j, k] = 1.0
                elif phi_val <= 1e-6:
                    flag_host[i, j, k] = C.CellFlag.TYPE_G
                    mass_host[i, j, k] = 0.0
                else:
                    flag_host[i, j, k] = C.CellFlag.TYPE_I
                    mass_host[i, j, k] = phi_val

    state = domain.state
    wp.copy(state.flag, wp.array(flag_host, dtype=wp.uint8, device=state.flag.device))
    wp.copy(state.phi, wp.array(phi_host, dtype=float, device=state.phi.device))
    wp.copy(state.mass, wp.array(mass_host, dtype=float, device=state.mass.device))
    _init_rest_state(domain, N)
    return flag_host, phi_host, mass_host


