"""Phase 2 golden-data regression tests for HOME-FSLBM free-surface.

Each test constructs a scene (droplet / flat interface / ellipsoid / …),
runs the full ``stream_collide_bvh + surface_1/2/3`` pipeline for a fixed
number of steps, and compares the final state against reference CUDA C++
output (golden ``.txt`` files).

The golden data is generated by the extended ``export_solver_golden.cpp``
in ``docs/papers/`` and stored as per-scene subdirectories under
``tests/golden_data/``.

.. note::

    **PLIC curvature offset**: the reference code and Warp share the same
    Monge-patch curvature formula with identical PLIC offset behaviour.
    The full-state regression tests below implicitly validate curvature
    (it feeds into the collision step via surface-tension Guo forcing),
    so matching f_mom_post / mass / phi / flag constitutes an end-to-end
    curvature verification.  Explicit cell-by-cell curvature comparison
    is in the companion ``test_regression_curvature.py``.
"""

