def _setup_near_droplets(domain, N, R, center_dist):
    """Two spheres of radius R, centres separated by center_dist along x."""
    import warp as wp

    cx1 = N / 2 - center_dist / 2
    cx2 = N / 2 + center_dist / 2
    cy = N / 2
    cz = N / 2

    delta = 1.5
    phi_host = np.zeros((N, N, N), dtype=np.float32)
    flag_host = np.zeros((N, N, N), dtype=np.uint8)
    mass_host = np.zeros((N, N, N), dtype=np.float32)

    for k in range(N):
        for j in range(N):
            for i in range(N):
                d1 = np.sqrt((i - cx1) ** 2 + (j - cy) ** 2 + (k - cz) ** 2)
                d2 = np.sqrt((i - cx2) ** 2 + (j - cy) ** 2 + (k - cz) ** 2)
                phi1 = (R - d1) / delta + 0.5
                phi2 = (R - d2) / delta + 0.5
                phi_raw = max(phi1, phi2)
                phi_val = max(0.0, min(1.0, phi_raw))
                phi_host[i, j, k] = phi_val
                if phi_val >= 1.0 - 1e-6:
                    flag_host[i, j, k] = C.CellFlag.TYPE_F
                    mass_host[i, j, k] = 1.0
                elif phi_val <= 1e-6:
                    flag_host[i, j, k] = C.CellFlag.TYPE_G
                    mass_host[i, j, k] = 0.0
                else:
                    flag_host[i, j, k] = C.CellFlag.TYPE_I
                    mass_host[i, j, k] = phi_val

    state = domain.state
    wp.copy(state.flag, wp.array(flag_host, dtype=wp.uint8, device=state.flag.device))
    wp.copy(state.phi, wp.array(phi_host, dtype=float, device=state.phi.device))
